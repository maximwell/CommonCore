#include "ft_printf.h"

void	ft_putstr_sp(char *s, int *count)
{
	int		i;

	if (s == NULL)
		s = "(null)";
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		(*count)++;
		i++;
	}
}
